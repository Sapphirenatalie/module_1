str_ = 'это моя строка'
print(str_[0])  # первый символ этой строки
print(str_[-1])  # последний символ этой строки
print(str_[2:5])  # с 3-го по 5-й
print(str_[::-1])  # это слово наоборот
print(len(str_))  # длина строки
